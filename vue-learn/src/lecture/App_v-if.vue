<template>
  <div>
    <h2 v-if="visible">Hello vue!</h2>
    <h2 v-else>Good night vue!</h2>
    <button @click="visible = !visible">toggle</button>
    <hr />
    <button @click="type='A'">A</button>
    <button @click="type='B'">B</button>
    <button @click="type='C'">C</button>
    <button @click="type=parseInt(Math.random*127);">Random</button>
    <h2 v-if="type === 'A'">A입니다.</h2>
    <h2 v-else-if="type === 'B'">B입니다.</h2>
    <h2 v-else-if="type === 'C'">C입니다.</h2>
    <h2 v-else>A,B,C가 아닙니다.</h2>
    <hr />
    <template v-if="visible">
      <h1>Title</h1>
      <p>Paragraph 1</p>
      <p>Paragraph 2</p>
    </template>
    <hr />
    <h1 v-show="ok">Title 입니다.</h1>
    <button @click="ok = !ok">toggle</button>
  </div>
</template>

<script>
import { ref } from 'vue';
export default {
setup(){
  const visible = ref(true);
  const type = ref('A');
  const ok = ref(true);
  return{visible,
  type,
  ok
}
}
}
</script>

